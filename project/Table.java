import java.util.*;
public class Table {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the value of n: ");
        int n = sc.nextInt();

        //loop
        for(int i =1; i<=10; i++){
            System.out.println(n*i);
         }
    }
}
